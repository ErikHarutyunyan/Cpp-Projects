#include <iostream>
#include <vector>

int main()
{
    int N;
    std::cin >> N;
    std::vector<int> dynArr(N);
    int count = 0;
    for (int i = 0; i < dynArr.size(); ++i)
        std::cin >> dynArr[i];
    for (int i = 0; i < dynArr.size(); i++)
    {
        for (int j = i + 1; j < dynArr.size(); j++)
        {
            if (dynArr[i] == dynArr[j])
            {
                for (int n = j - 1; n >= 0; n--)
                {
                    if (dynArr[j] == dynArr[n])
                        count++;
                }
                if (count == 1)
                {
                    std::cout << dynArr[i] << " ";
                }
                count = 0;
            }
        }
    }
}